package registroresolucion;

public class RecursoResolucion {
	
	public Long idRecurso;
	public String codigoAntiguo;
	public String descripcion;
	
	public RecursoResolucion(Long idRecurso, String codigoAntiguo, String descripcion) {
		this.idRecurso = idRecurso;
		this.codigoAntiguo = codigoAntiguo;
		this.descripcion = descripcion;
	}
	
}
